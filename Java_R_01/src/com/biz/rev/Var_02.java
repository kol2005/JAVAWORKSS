package com.biz.rev;

public class Var_02 {

	public static void main(String[] args) {
		
		// 3개의 변수를 선언
		int num1;
		int num2;
		int sum;
		
		// 앞에서 선언된 변수 num1 과 num2 에
		// 각각 40과 30을 보관하라
		num1 = 40;
		num2 = 30;
		
		// num1 과 num2 에 보관중인 값을 읽어서
		// 덧셈을 수행한 후 
		// 앞에서 선언된 변수 sum 에 보관하라
		sum = num1 + num2;

		// 결과를 console 에 보여라
		System.out.println(sum);
	}

}
