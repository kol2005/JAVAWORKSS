package com.biz.rev;

public class Var_03 {

	public static void main(String[] args) {
		
		// 문자열(형) 변수 s를 선언하고
		// 문자열 KOREA 를 보관하라
		String s = "KOREA";
		
		// 앞에서 선언된 변수 s 에
		// 문자열 "대한" 과 "민국" 을 덧셈(= 연결하라)하여
		// 변수 s 에 보관하라 
		s = "대한" + "민국";
		
		// 변수 s 에 보관중인 값을 console 에 보여라
		System.out.println(s);//대한민국

	}

}
